async fn validate_python(code: &str) -> Result<(), String> {
    use std::process::Stdio;
    let mut child = tokio::process::Command::new("python3")
        .args(["-c", "import ast,sys; ast.parse(sys.stdin.read())"])
        .stdin(Stdio::piped())
        .stdout(Stdio::null())
        .stderr(Stdio::piped())
        .spawn()
        .map_err(|e| format!("python3 not found: {e}"))?;

    if let Some(mut stdin) = child.stdin.take() {
        let _ = stdin.write_all(code.as_bytes()).await;
        // stdin dropped here → EOF sent to python3
    }
    let output = child.wait_with_output().await.map_err(|e| e.to_string())?;
    if output.status.success() {
        Ok(())
    } else {
        let msg = String::from_utf8_lossy(&output.stderr).to_string();
        Err(msg.trim().to_string())
    }
}

fn strip_fences(s: &str) -> String {
    let s = s.trim();
    // Find first code fence anywhere in the response (handles prose before the block)
    if let Some(fence_pos) = s.find("```") {
        let after_fence = &s[fence_pos..];
        let code_start = fence_pos + after_fence.find('\n').unwrap_or(after_fence.len()) + 1;
        if code_start >= s.len() { return String::new(); }
        let code = s[code_start..].trim_end();
        return if code.ends_with("```") {
            code[..code.len()-3].trim_end().to_string()
        } else {
            code.to_string()
        };
    }
    s.to_string()
}

fn between<'a>(s: &'a str, open: &str, close: &str) -> Option<&'a str> {
    let i = s.find(open)? + open.len();
    Some(&s[i..i + s[i..].find(close)?])
}

fn process_glue(glue: &str) -> String {
    let mut g = glue.to_string();
    if let Some(s) = g.find("/* @ts-self-types") {
        if let Some(r) = g[s..].find("*/") {
            g = format!("{}{}", &g[..s], g[s+r+2..].trim_start_matches('\n'));
        }
    }
    g = g.replace("export function ", "function ");
    g = g.lines()
        .filter(|l| !l.trim_start().starts_with("export {"))
        .collect::<Vec<_>>().join("\n");
    g = g.replace(
        "module_or_path = new URL('bruecke_bg.wasm', import.meta.url);",
        "throw new Error('bruecke: WASM loaded via fetch(/bruecke_bg.wasm)');",
    );
    g.replace("async function __wbg_init(", "async function init(")
}

/// Returns true if the path contains no `..` components, is non-empty, and is not absolute.
pub fn is_safe_image_path(path: &str) -> bool {
    if path.is_empty() { return false; }
    if path.starts_with('/') { return false; }
    !path.split('/').any(|seg| seg == "..")
}

/// GET /images/*path — serves image files from CWD (next to app.py).
async fn images_handler(axum::extract::Path(path): axum::extract::Path<String>) -> Response {
    if !is_safe_image_path(&path) {
        return (StatusCode::BAD_REQUEST, "invalid path").into_response();
    }
    let file_path = std::path::Path::new(".").join(&path);
    match tokio::fs::read(&file_path).await {
        Err(_) => (StatusCode::NOT_FOUND, "not found").into_response(),
        Ok(bytes) => {
            let mime = match file_path.extension().and_then(|e| e.to_str()) {
                Some("png")  => "image/png",
                Some("jpg") | Some("jpeg") => "image/jpeg",
                Some("gif")  => "image/gif",
                Some("webp") => "image/webp",
                Some("svg")  => "image/svg+xml",
                _            => "application/octet-stream",
            };
            (
                StatusCode::OK,
                [(header::CONTENT_TYPE, mime)],
                bytes,
            ).into_response()
        }
    }
}

