#[derive(Clone)]
struct S {
    client:       reqwest::Client,
    tx:           broadcast::Sender<String>,
    last:         Arc<Mutex<String>>,
    result:       Arc<Mutex<String>>,
    last_history: Arc<Mutex<String>>,   // dedup: only save history when source changes
    prompter:     bool,                 // show AI bar in browser?
}

impl S {
    fn new(prompter: bool) -> Self {
        S {
            client:       reqwest::Client::new(),
            tx:           broadcast::channel(128).0,
            last:         Arc::new(Mutex::new(String::new())),
            result:       Arc::new(Mutex::new(r#"{"ok":false}"#.into())),
            last_history: Arc::new(Mutex::new(String::new())),
            prompter,
        }
    }
}

/// Prepend any local `import X` found in `source` where `X.py` exists on disk.
/// Both files run in the same global scope, so frame globals (keys, mouse_x …)
/// are available inside the bundled module without any extra wiring.
fn bundle_source(source: &str) -> String {
    let mut seen   = std::collections::HashSet::new();
    let mut prefix = String::new();

    for line in source.lines() {
        let t = line.trim_start();
        let name = if let Some(rest) = t.strip_prefix("import ") {
            rest.split(|c: char| !c.is_alphanumeric() && c != '_')
                .next().unwrap_or("").trim()
        } else if let Some(rest) = t.strip_prefix("from ") {
            rest.split_whitespace().next().unwrap_or("").split('.').next().unwrap_or("")
        } else {
            continue;
        };

        if name.is_empty() || seen.contains(name) { continue; }
        let path = std::path::Path::new(name).with_extension("py");
        if let Ok(content) = std::fs::read_to_string(&path) {
            eprintln!("[bundle] inlining {}.py ({} bytes)", name, content.len());
            seen.insert(name.to_string());
            prefix.push_str(&format!("# ===== {name}.py =====\n"));
            prefix.push_str(&content);
            if !content.ends_with('\n') { prefix.push('\n'); }
            prefix.push_str(&format!("# ===== end {name}.py =====\n\n"));
        }
    }

    if prefix.is_empty() { source.to_string() } else { format!("{prefix}{source}") }
}

fn broadcast_source(s: &S, source: String) {
    // history saves the raw source (what the developer wrote)
    if let Ok(mut last) = s.last_history.lock() {
        if *last != source {
            *last = source.clone();
            save_history(&source);
        }
    }
    // browser receives the bundled version (local imports inlined)
    let bundled = bundle_source(&source);
    if let Ok(mut l) = s.last.lock() { *l = bundled.clone(); }
    match s.tx.send(bundled) {
        Ok(n)  => eprintln!("[sse] pushed app.py ({n} receiver(s))"),
        Err(_) => eprintln!("[sse] no browser connected — will replay on connect"),
    }
}

