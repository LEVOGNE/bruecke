/// GET / — serve HTML with embedded JS; WASM loaded via fetch (streaming, fast)
async fn index(State(s): State<S>) -> Response {
    let glue   = process_glue(GLUE_RAW);
    let css    = between(SHELL, "<style>", "</style>").unwrap_or("");
    let body   = between(SHELL, "<body>", "<script").unwrap_or("").trim();

    // In dev: optionally override engine.js from disk (live editing without rebuild)
    let engine_disk = std::fs::read_to_string("src/engine.js").ok();
    let engine = engine_disk.as_deref().unwrap_or(ENGINE);

    // Hide AI bar when running in API-only mode
    let no_prompter_css = if !s.prompter {
        "<style>#ai-bar,#ahp,#asp{display:none!important}</style>"
    } else {
        ""
    };

    let html = format!(r#"<!DOCTYPE html><html lang="en"><head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width,initial-scale=1.0">
<title>bruecke</title>
<style>{css}</style>
{no_prompter_css}</head><body>
{body}
<script>
function js_now(){{return performance.now();}}
{glue}
{engine}
(async()=>{{ try {{ await _brueckeStart(); }} catch(e) {{ fatal(e); }} }})();
</script></body></html>"#);

    Response::builder()
        .status(200)
        .header("Content-Type", "text/html; charset=utf-8")
        .body(Body::from(html))
        .unwrap()
}

