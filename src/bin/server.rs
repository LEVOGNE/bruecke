/// POST /run — manual Python source push
async fn run(State(s): State<S>, body: Bytes) -> impl IntoResponse {
    let source = match std::str::from_utf8(&body) {
        Ok(s) => s.trim().to_string(),
        Err(_) => return (StatusCode::BAD_REQUEST, "invalid utf-8"),
    };
    if source.is_empty() { return (StatusCode::BAD_REQUEST, "empty body"); }
    broadcast_source(&s, source);
    (StatusCode::OK, "ok")
}

/// GET /events — SSE with replay
async fn events(
    State(s): State<S>,
) -> Sse<impl tokio_stream::Stream<Item = Result<Event, std::convert::Infallible>>> {
    eprintln!("[sse] browser connected");
    let last = s.last.lock().map(|l| l.clone()).unwrap_or_default();
    let replay = if last.is_empty() { vec![] } else {
        vec![Ok(Event::default().data(last))]
    };
    let live = BroadcastStream::new(s.tx.subscribe())
        .filter_map(|r| r.ok())
        .map(|src| Ok::<Event, std::convert::Infallible>(Event::default().data(src)));
    Sse::new(tokio_stream::iter(replay).chain(live)).keep_alive(KeepAlive::default())
}

async fn result_post(State(s): State<S>, body: Bytes) -> impl IntoResponse {
    if let Ok(mut r) = s.result.lock() { *r = String::from_utf8_lossy(&body).to_string(); }
    StatusCode::OK
}

async fn result_get(State(s): State<S>) -> impl IntoResponse {
    let json = s.result.lock().map(|r| r.clone())
        .unwrap_or_else(|_| r#"{"ok":false,"error":"mutex poisoned"}"#.into());
    (StatusCode::OK, [(header::CONTENT_TYPE, "application/json")], json)
}

